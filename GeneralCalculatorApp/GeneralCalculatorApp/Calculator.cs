using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GeneralCalculatorApp
{
    class Calculator
    {
        public double Add(double fstNo, double scndNo)
        {
            return fstNo + scndNo;
        }
        public double Subtract(double fstNo, double scndNo)
        {
            return fstNo - scndNo;
        }
        public double Multiply(double fstNo, double scndNo)
        {
            return fstNo * scndNo;
        }
        public double Devide(double fstNo, double scndNo)
        {
            return fstNo / scndNo;
        }
    }
}
